const number_1 = parseFloat(prompt('Digite um número:'))
const number_2 = parseFloat(prompt('Digite outro número:'))

let soma = number_1 + number_2
let subt = number_1 - number_2
let mult = number_1 * number_2
let div = number_1 / number_2

alert(`A soma entre esses dois números é: ${soma}
A subtração entre esses dois números é: ${subt}
A multiplicação entre esses dois números é: ${mult}
A divisão entre esses dois números é: ${div}`)